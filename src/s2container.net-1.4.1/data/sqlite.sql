CREATE TABLE DEPT (
	DEPTNO INT PRIMARY KEY,
	DNAME VARCHAR(14),
	LOC VARCHAR(13),
	VERSIONNO INT,
	ACTIVE INT
);

CREATE TABLE DEPT2 (
	DEPTNO INT PRIMARY KEY,
	DNAME VARCHAR(14),
	ACTIVE INT
);

CREATE TABLE EMP (
	EMPNO INT PRIMARY KEY,
	ENAME VARCHAR(10),
	JOB VARCHAR(9),
	MGR INT,
	HIREDATE TIMESTAMP,
	SAL INT,
	COMM INT,
	DEPTNO INT,
	TSTAMP TIMESTAMP
);

CREATE TABLE EMP2 (
	EMPNO INT PRIMARY KEY,
	ENAME VARCHAR(10),
	DEPTNUM INT
);

CREATE TABLE EMP_NULLABLE (
	EMPNO INT PRIMARY KEY,
	ENAME VARCHAR(10),
	JOB VARCHAR(9),
	MGR INT,
	HIREDATE TIMESTAMP,
	SAL INT,
	COMM INT,
	DEPTNO INT,
	TSTAMP TIMESTAMP,
	NULLABLENEXTRESTDATE TIMESTAMP
);

CREATE TABLE GENERIC_NULLABLE (
	ID INTEGER PRIMARY KEY AUTOINCREMENT,
	DDATE TIMESTAMP,
	ENTITYNO INT
);

CREATE TABLE UNDER_SCORE (
	UNDER_SCORE_NO INT PRIMARY KEY,
	TABLE_NAME VARCHAR(50),
	"TABLE_NAME_" VARCHAR(50),
	"_TABLE_NAME" VARCHAR(50),
	"_TABLE_NAME_" VARCHAR(50)
);

CREATE TABLE IDTABLE (
	ID INTEGER PRIMARY KEY AUTOINCREMENT,
	ID_NAME VARCHAR(20)
);

CREATE TABLE BASICTYPE (
	ID DECIMAL (18, 0) PRIMARY KEY,
	BOOLTYPE DECIMAL (1, 0),
	BYTETYPE INT,
	SBYTETYPE INT,
	INT16TYPE INT,
	INT32TYPE INT,
	INT64TYPE BIGINT,
	SINGLETYPE FLOAT,
	DOUBLETYPE DOUBLE PRECISION,
	DECIMALTYPE DECIMAL (28, 0),
	STRINGTYPE VARCHAR (1024),
	DATETIMETYPE TIMESTAMP
);

